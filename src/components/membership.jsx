import React, { useState, useEffect } from 'react';
import Head from 'next/head';
import styles from '@styles/Membership.module.css';
import { usePlayers } from '@hooks/players';

export default function Membership() {
	const { getUsers, addUser, data, isLoading, message } = usePlayers();
	const [user, setUser] = useState({
		firstName: '',
		lastName: '',
		email: '',
		level: '',
		referrer: '',
	});

	const { firstName, lastName, email, level, referrer } = user;

	const saveUser = async (event) => {
		event.preventDefault();

		await addUser(user);

		// Reset user fields
		setUser({
			firstName: '',
			lastName: '',
			email: '',
			level: '',
			referrer: '',
		});
	};

	useEffect(() => {
		getUsers({ approved: true });
	}, []);

	return (
		<>
			<Head>
				<title>Create Next App</title>
				<meta name='description' content='Generated by create next app' />
				<meta name='viewport' content='width=device-width, initial-scale=1' />
				<link rel='icon' href='/favicon.ico' />
			</Head>
			<main className={styles.register}>
				<p>Players in the Team: {data.length}</p>
				<form name='' onSubmit={saveUser}>
					<label>
						<input
							type='text'
							placeholder='First Name'
							value={firstName}
							onChange={(event) => setUser({ ...user, firstName: event.target.value })}
							required
						/>
					</label>
					<label>
						<input
							type='text'
							placeholder='Last Name'
							value={lastName}
							onChange={(event) => setUser({ ...user, lastName: event.target.value })}
							required
						/>
					</label>
					<label>
						<input
							type='email'
							placeholder='Email'
							value={email}
							onChange={(event) => setUser({ ...user, email: event.target.value })}
							required
						/>
					</label>
					<label>
						<input
							type='number'
							placeholder='Level'
							step='0.5'
							value={level}
							onChange={(event) => setUser({ ...user, level: event.target.value })}
							required
						/>
					</label>
					<label>
						<input
							type='text'
							placeholder='Referrer'
							value={referrer}
							onChange={(event) => setUser({ ...user, referrer: event.target.value })}
						/>
					</label>
					<input type='submit' disabled={isLoading} />
				</form>
				{message?.message && <p>{message?.message}</p>}
			</main>
		</>
	);
}
